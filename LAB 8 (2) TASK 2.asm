.MODEL SMALL

.STACK 100H

.DATA

MSG DB 'MY NAME IS UMAR','$'
MSG1 DB 'I LIVE IN LAHORE','$'

.CODE

MAIN PROC FAR

MOV AX,@DATA
MOV DS,AX

MOV AX,0000H

MOV AX,OFFSET MSG
MOV BX,OFFSET MSG1

PUSH AX
PUSH BX

CALL PRINT

.EXIT

MAIN ENDP

PRINT PROC

PUSH BP
MOV BP,SP

MOV AH,02H ;MOVING CURSOR TO ROW NO 0 AND COL TO 2
MOV BH,00
MOV DL,02H
MOV DH,00H
INT 10H

MOV AH,09H   ;DISPLAYING FIRST MESSAGE
MOV DX,[BP+6]
INT 21H

MOV AH,02H ;MOVING CURSOR TO ROW NO 2 AND COL TO 2 HENCE USING THE FUNCTIONALITY OF NEW LINE
MOV BH,00
MOV DL,2H
MOV DH,2H
INT 10H

MOV AH,09H
MOV DX,[BP+4] ;DISPLAYING SECOND MESSAGE
INT 21H

MOV AH,02H ;MOVING CURSOR TO THE BEGINING OF THE LINE HENCE USING THE FUNCTIONALITY OF CARRIAGE RETURN  
MOV BH,00H
MOV DL,00H
MOV DH,00H
INT 10H

MOV AH,02  ;DISPLAYING SINGLE CHARACTER
MOV DL,'1'
INT 21H

MOV AH,02H  ;SAME WORK AS ABOVE CARRIAGE RETURN
MOV BH,00H
MOV DL,00H
MOV DH,02H
INT 10H

MOV AH,02H  ;SAME AS ABOVE
MOV DL,'2'
INT 21H 

MOV AH,02H  ;USING THE FUNCTIONALITY OF TAB TO MOVE 8 SPACES
MOV BH,00H
MOV DL,08H
MOV DH,00H
INT 10H
             
MOV AH,02H
MOV DL,'O'
INT 21H

MOV AH,02H  ;SAME AS ABOVE
MOV BH,00H
MOV DL,08H
MOV DH,2H
INT 10H

MOV AH,02H
MOV DL,'E'
INT 21H  

POP BP
    
RET    
    
PRINT ENDP    