.MODEL SMALL

.STACK 100H

.DATA

MSG DB 'MY NAME IS UMAR','$'
MSG1 DB 'I LIVE IN LAHORE','$'

.CODE

MAIN PROC FAR

MOV AX,@DATA
MOV DS,AX

MOV AX,0000H

MOV AX,OFFSET MSG
MOV BX,OFFSET MSG1

PUSH AX
PUSH BX

CALL PRINT

.EXIT

MAIN ENDP

PRINT PROC
    
    
MOV CL,80
PUSH BP
MOV BP,SP 
MOV AX,0000H
MOV BX,0000H

REPEAT:

MOV AH,09H   ;DISPLAYING FIRST MESSAGE
MOV DX,[BP+6]
INT 21H

MOV AH,02H ;MOVING CURSOR TO ROW NO 2 AND COL TO 2 HENCE USING THE FUNCTIONALITY OF NEW LINE
MOV BH,00
MOV DL,CH
MOV DH,BL
INT 10H

MOV AH,09H
MOV DX,[BP+4] ;DISPLAYING SECOND MESSAGE
INT 21H

ADD CH,20
SUB CL,20

JNZ REPEAT

INC BL

CALL CURSOR

JMP REPEAT

POP BP
    
RET    
    
PRINT ENDP

CURSOR PROC
    
MOV AH,06H
MOV AL,00H
MOV BH,07H
MOV CH,01H
MOV CL,00H
MOV DH,24H
MOV DL,79H
INT 10H

RET

CURSOR ENDP    