.MODEL SMALL

.STACK 100H

.DATA


INPUT DB 3,?,3 DUP(0FFH)

VARIABLE DW 0000H

.CODE

MAIN PROC FAR

MOV AX,@DATA
MOV DS,AX
MOV AX,0B800H
MOV ES,AX

CALL TAKE_INPUT

CALL CONVERT_NUMBER

CALL SHOW_DISPLAY

MOV AH,4CH
INT 21H

MAIN ENDP


TAKE_INPUT PROC    

MOV AH,0AH
MOV DX,OFFSET INPUT
INT 21H

RET

TAKE_INPUT ENDP

CONVERT_NUMBER PROC
    
MOV AH,INPUT[SI+2]
MOV AL,INPUT[SI+3]

SUB AH,30H
SUB AL,30H

MOV VARIABLE,AX    

RET

CONVERT_NUMBER ENDP

SHOW_DISPLAY PROC
    
MOV AX,VARIABLE

ADD AH,30H
ADD AL,30H

MOV ES:[SI+4],AH
MOV ES:[SI+6],AL

RET 

SHOW_DISPLAY ENDP

    