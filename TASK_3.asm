.MODEL SMALL

.STACK 100H

.DATA


NUMBER1 DB 3,?,3 DUP(0FFH)
NUMBER2 DB 3,?,3 DUP(0FFH)

VARIABLE DW 0000H

.CODE

MAIN PROC FAR

MOV AX,@DATA
MOV DS,AX
MOV AX,0B800H
MOV ES,AX

CALL TAKE_INPUT

CALL CONVERT_NUMBER

CALL SHOW_DISPLAY

MOV AH,4CH
INT 21H

MAIN ENDP


TAKE_INPUT PROC    

MOV AH,0AH
MOV DX,OFFSET NUMBER1
INT 21H

MOV DX,OFFSET NUMBER2
INT 21H

RET

TAKE_INPUT ENDP

CONVERT_NUMBER PROC
    
MOV AH,NUMBER1[SI+2]
MOV AL,NUMBER1[SI+3]
MOV BH,NUMBER2[SI+2]
MOV BL,NUMBER2[SI+3]

SUB AH,30H
SUB AL,30H
SUB BH,30H
SUB BL,30H

ADD AX,BX

ADD AH,30H
ADD AL,30H

MOV VARIABLE,AX    

RET

CONVERT_NUMBER ENDP

SHOW_DISPLAY PROC

MOV ES:[SI+8],AH
MOV ES:[SI+10],AL

RET    
 

SHOW_DISPLAY ENDP

    