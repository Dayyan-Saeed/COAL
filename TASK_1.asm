.MODEL SMALL

.STACK 100H

.DATA


INPUT DB 3,?,3 DUP(0FFH)

VARIABLE DW 0000H

.CODE

MAIN PROC FAR

MOV AX,@DATA
MOV DS,AX

CALL TAKE_INPUT

CALL CONVERT_NUMBER

MOV AH,4CH
INT 21H

MAIN ENDP


TAKE_INPUT PROC    

MOV AH,0AH
MOV DX,OFFSET INPUT
INT 21H

RET

TAKE_INPUT ENDP

CONVERT_NUMBER PROC
    
MOV AH,INPUT[SI+2]
MOV AL,INPUT[SI+3]

SUB AH,30H
SUB AL,30H

MOV VARIABLE,AX    

RET

CONVERT_NUMBER ENDP