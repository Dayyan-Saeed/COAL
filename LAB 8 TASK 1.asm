.MODEL SMALL

.STACK 100H

.DATA

MSG DB "HELLO",0

.CODE

MAIN PROC
    
MOV AX,@DATA
MOV DS,AX
MOV AX,0B800H
MOV ES,AX

MOV AH,MSG[SI]
MOV AL,MSG[SI+1]

PUSH AX

MOV AH,MSG[SI+2]
MOV AL,MSG[SI+3]

PUSH AX

MOV AH,MSG[SI+4]
MOV AL,MSG[SI+5]

PUSH AX

MOV AX,0000H

CALL PRINT

POP BX
POP BX
POP BX

.EXIT

MAIN ENDP

PRINT PROC
    
PUSH BP
MOV BP,SP
MOV DI,8

REPEAT1:

MOV AL,[BP+DI+1]

MOV AH,07
              
MOV ES:[SI],AX

MOV AL,[BP+DI]

ADD SI,2

MOV ES:[SI],AX

ADD SI,2
SUB DI,2


CMP AL,0

JNE REPEAT1

POP BP

RET

PRINT ENDP    